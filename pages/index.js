import Head from "next/head";
import Image from "next/image";
import styles from "../styles/Home.module.css";
import React, { useState, useRef } from "react";
import Cropper from "react-cropper";
import "cropperjs/dist/cropper.css";
import Footer from "../components/Footer";

const hashnode = {
  width: 720,
  height: 480,
  // ratio: this.width / this.height,
};
const dev = {};
const medium = {};

const defaultImage = "https://picsum.photos/seed/picsum/720/480";

export default function Home() {
  const [image, setImage] = useState(defaultImage);
  const [cropper, setCropper] = useState({
    width: hashnode.width,
    height: hashnode.height,
  });

  const changeCropperDimensions = () => {
    console.log("Hashnode clicked");
  };

  const cropperRef = useRef();
  const onCrop = () => {
    let imageElement =
      cropperRef === null || cropperRef === 0 ? 0 : cropperRef.current;
    let cropper =
      imageElement === null || imageElement === 0 ? 0 : imageElement.cropper;
    // console.log(cropper.getCroppedCanvas().toDataURL());
  };

  const onChange = (e) => {
    e.preventDefault();
    let files;
    if (e.dataTransfer) {
      files = e.dataTransfer.files;
    } else if (e.target) {
      files = e.target.files;
    }
    const reader = new FileReader();
    reader.onload = () => {
      setImage(reader.result);
    };
    reader.readAsDataURL(files[0]);
  };
  return (
    <div className={styles.wrapper}>
      <Head>
        <title>Blog cover resizer</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <h1>The image resizer</h1>
      <p>Leave the image resizing issues at bay</p>
      <div
        style={{
          width: `${cropper.width}px`,
          margin: "0 auto",
          marginTop: "30px",
        }}
      >
        <p>Select the image you want to crop</p>
        <input type="file" onChange={onChange} />
        <button>Use test image</button>
        <Cropper
          src={image}
          style={{ height: cropper.height, width: "100%" }}
          // Cropper.js options
          initialAspectRatio={16 / 9}
          guides={true}
          crop={onCrop}
          ref={cropperRef}
          disable={false}
          // viewMode={1}
          // minCropBoxHeight={10}
          // minCropBoxWidth={10}
          // background="black"
          responsive={true}
          movable={true}
          // autoCropArea={1}
          checkOrientation={false}
        />
      </div>
      <button>Drag mode</button>
      <button>Crop mode</button>
      <h3 onClick={changeCropperDimensions}>Hashnode</h3>
      <h3>DEV</h3>
      <Footer />
    </div>
  );
}
